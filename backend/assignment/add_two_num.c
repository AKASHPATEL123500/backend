

#include <stdio.h>

int main(){

    // initializing variables
    int num1 , num2 , sum ;

    // taking input from the user
    printf("Enter two integers numbers: ");
    scanf("%d %d", &num1 , &num2);

    // adding two numbers using third variable
    sum = num1 + num2 ;

    // Displaying result
    printf("Sum of two numbers: %d", sum);
    
    return 0 ; 
}


// OutPut Example:
// Enter two integers numbers: 5 10
// Sum of two numbers: 15


// Explanation:
// This program takes two integer inputs from the user, adds them together, and displays the result.


